
# print whole numbers, decimal numbers and negative numbers
print(3)
print(3.1415926)
print(-3.14159)

# work with numbers
print(3 * 4) # 12
print(3 * 4 + 5) # 17
print(3 * (4 + 5)) # 27

print(10/3) # 3.33333335
print(10 % 3) # 1

# store numbers in variables
my_num = 22;
print(my_num)

# convert number to string
print(str(my_num))
print("My number is: " + str(my_num))


# Absolute Value
number = -5;
print(abs(number))

# Power Of
print(pow(2, 6))

# Get highest number
print(max(4, 6))

# Get lowest number
print(min(4, 6))

# Round numbers
print(round(3.14159))


# import math module
from math import *

# Floor (Round down)
print(floor(3.7))

# Ceil (Round up)
print(ceil(3.7))

# Square Root
print(sqrt(36))
