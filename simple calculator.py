
# int only allows whole numbers
num1 = input("Please enter a number: ")
num2 = input("Please enter another number: ")

result = int(num1) + int(num2)

print(result)


# float allows for decimals
num1 = input("Please enter a number: ")
num2 = input("Please enter another number: ")

result = float(num1) + float(num2)

print(result)
